import numpy as np
x = np.array([2, 4, 6])
y = np.array([1, 2, 3])

# Elementwise operations
x + y         
x - y        
x * y       
x / y       
x > 3        
x ** 2     

print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)         
print("x > 3 =", x > 3)
print("x ** 2 =", x ** 2)

